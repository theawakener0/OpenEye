// GetCorpusByComplexity filters queries by complexity level.
func GetCorpusByComplexity(level string) []LabeledQuery {
	corpus := GetComplexityCorpus()
	result := make([]LabeledQuery, 0)
	for _, q := range corpus {
		if q.ComplexityLabel == level {
			result = append(result, q)
		}
	}
	return result
}

// GetCorpusStats returns statistics about the corpus.
func GetCorpusStats() map[string]int {
	corpus := GetComplexityCorpus()
	stats := map[string]int{
		"total":   len(corpus),
		"simple":  0,
		"medium":  0,
		"complex": 0,
	}
	for _, q := range corpus {
		stats[q.ComplexityLabel]++
	}
	return stats
}
